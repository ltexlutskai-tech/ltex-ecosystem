/** Канонічний набір kind-string-ів, які мапляться на brand-glyph icon. */
export type BrandIconKind =
  | "viber"
  | "telegram"
  | "whatsapp"
  | "instagram"
  | "tiktok"
  | "youtube"
  | "facebook"
  | "pinterest"
  | "link";

const ICON_MAP: Record<BrandIconKind, (p: IconProps) => ReactElement> = {
  viber: ViberIcon,
  telegram: TelegramIcon,
  whatsapp: WhatsAppIcon,
  instagram: InstagramIcon,
  tiktok: TikTokIcon,
  youtube: YouTubeIcon,
  facebook: FacebookIcon,
  pinterest: PinterestIcon,
  link: LinkIcon,
};

/**
 * Нормалізує довільний kind-string (1С network code / messenger value) до
 * відомого `BrandIconKind`; невідоме → `link`.
 */
export function resolveBrandIconKind(
  kind: string | null | undefined,
): BrandIconKind {
  if (!kind) return "link";
  const k = kind.toLowerCase().trim();
  if (k in ICON_MAP) return k as BrandIconKind;
  return "link";
}

/**
 * Helper-компонент: рендерить brand-glyph icon за kind-string-ом.
 * Невідомий kind → generic «link» fallback.
 */
export function BrandIcon({
  kind,
  className,
  ...rest
}: { kind: string | null | undefined } & IconProps) {
  const resolved = resolveBrandIconKind(kind);
  const Icon = ICON_MAP[resolved];
  return <Icon className={className} {...rest} />;
}
